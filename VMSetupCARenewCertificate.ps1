        @{
            'MD2'    = '32769'
            'MD4'    = '32770'
            'MD5'    = '32771'
            'SHA1'   = '32772'
            'SHA256' = '0xffffffff'
            'SHA384' = '0xffffffff'
            'SHA512' = '0xffffffff'
