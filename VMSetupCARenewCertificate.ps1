                if (-not (Test-Path -Path "$CertEnrollDirectory\$CACommonName.p12") -and
                    (ShouldProcess @WhatIfSplat -Message "Backing up CA private key to `"$CertEnrollDirectory\$CACommonName.p12`"" @VerboseSplat))
