function Restart-CertSvc
{
    if (ShouldProcess @WhatIfSplat -Message "Restarting CA service..." @VerboseSplat)
    {
        TryCatch { Restart-Service -Name certsvc } -ErrorAction Stop > $null

        Start-Sleep -Seconds 3

        if ((Get-Service -Name CertSvc).Status -ne 'Running')
        {
           Write-Warning -Message "Can't start CertSvc, unknown error."
        }
    }
}

