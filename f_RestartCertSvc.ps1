 .DESCRIPTION
    Restart Active Directory Certificate Services (certsvc)
 .COMPONENT
    Depends on f_TryCatch.ps1
    Depends on f_ShouldProcess.ps1
 .NOTES
    AUTHOR Jonas Henriksson
 .LINK
    https://github.com/J0N7E
