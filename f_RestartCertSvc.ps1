<#
 .DESCRIPTION
    Restart Active Directory Certificate Services (certsvc)
 .COMPONENT
    Depends on f_TryCatch.ps1
    Depends on f_ShouldProcess.ps1
 .NOTES
    AUTHOR Jonas Henriksson
 .LINK
    https://github.com/J0N7E
#>

function Restart-CertSvc
{
    if (ShouldProcess @WhatIfSplat -Message "Restarting CA service..." @VerboseSplat)
    {
        TryCatch { Restart-Service -Name certsvc } -ErrorAction Stop > $null

        Start-Sleep -Seconds 5

        if ((Get-Service -Name CertSvc | Select-Object -ExpandProperty Status) -ne 'Running')
        {
           Write-Warning -Message "Can't start CertSvc..."

           foreach($Event in (Get-WinEvent -ProviderName Microsoft-Windows-CertificationAuthority -MaxEvents 2))
           {
                Write-Warning -Message "TimeCreated: $($Event.TimeCreated)"
                Write-Warning -Message "Id: $($Event.Id)"
                Write-Warning -Message "Message: $($Event.Message)"
                Write-Host
           }
        }
    }
}

