function TryCatch
{
    [cmdletbinding()]

    param
    (
        [ScriptBlock]$ScriptBlock,
        [Object]$ArgumentList
    )

    # Reset errorlevel
    $Global:LASTEXITCODE = 0
    $ErrorCode = 0

    try
    {
        # Invoke scriptblock
        $Output = Invoke-Command -ScriptBlock $ScriptBlock -ArgumentList $ArgumentList

        # Check if errorlevel changed
        if ($LASTEXITCODE -ne 0)
        {
            $ErrorCode = $LASTEXITCODE
        }
    }
    catch
    {
        $ErrorCode = 1
        $Output = $_
    }

    # Convert arraylist to array
    if ($Output -and $Output.GetType() -eq 'ArrayList')
    {
        $Output = $Output.GetArray()
    }

    if ($ErrorCode)
    {
        Write-Warning -Message $Output
        Write-Error   -Message $Output -ErrorId $ErrorCode
    }
    else
    {
        Write-Output -InputObject $Output
    }
}

