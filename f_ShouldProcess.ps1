function ShouldProcess
{
    [cmdletbinding(SupportsShouldProcess=$true)]

    param
    (
        [String]$Message
    )

    if ($WhatIfPreference)
    {
        if ($Message)
        {
            Write-Host "What If: `"$Message`""
        }

        Write-Output -InputObject $false
    }
    else
    {
        if ($Message)
        {
            Write-Verbose $Message
        }

        Write-Output -InputObject $true
    }
}

