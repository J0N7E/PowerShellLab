[cmdletbinding(SupportsShouldProcess=$true)]

Param
(
    # VM name
    [String]$VMName,
    # Computer name
    [String]$ComputerName,
    # Force
    [Switch]$Force,

    # Serializable parameters
    $Session,
    $Credential,
    $DomainCredential,

    # New name to set
    [String]$NewName,

    # Name of domain to join
    [String]$JoinDomain,

    # Restart
    [Switch]$Restart
)

Begin
{
    # ██████╗ ███████╗ ██████╗ ██╗███╗   ██╗
    # ██╔══██╗██╔════╝██╔════╝ ██║████╗  ██║
    # ██████╔╝█████╗  ██║  ███╗██║██╔██╗ ██║
    # ██╔══██╗██╔══╝  ██║   ██║██║██║╚██╗██║
    # ██████╔╝███████╗╚██████╔╝██║██║ ╚████║
    # ╚═════╝ ╚══════╝ ╚═════╝ ╚═╝╚═╝  ╚═══╝

    # Check if new name exist
    if (-not $NewName)
    {
        if ($VMName)
        {
            $NewName = $VMName
        }
        elseif ($ComputerName)
        {
            $NewName = $ComputerName
        }
    }

    ##############
    # Deserialize
    ##############

    $Serializable =
    @(
        @{ Name = 'Session';                                  },
        @{ Name = 'Credential';         Type = [PSCredential] },
        @{ Name = 'DomainCredential';   Type = [PSCredential] }
    )

    #########
    # Invoke
    #########

    Invoke-Command -ScriptBlock `
    {
        try
        {
            . $PSScriptRoot\s_Begin.ps1
            . $PSScriptRoot\f_CheckContinue.ps1
        }
        catch [Exception]
        {
            throw "$_ $( $_.ScriptStackTrace)"
        }

    } -NoNewScope

    # ███╗   ███╗ █████╗ ██╗███╗   ██╗
    # ████╗ ████║██╔══██╗██║████╗  ██║
    # ██╔████╔██║███████║██║██╔██╗ ██║
    # ██║╚██╔╝██║██╔══██║██║██║╚██╗██║
    # ██║ ╚═╝ ██║██║  ██║██║██║ ╚████║
    # ╚═╝     ╚═╝╚═╝  ╚═╝╚═╝╚═╝  ╚═══╝

    $MainScriptBlock =
    {
        # Initialize
        $Result = @{}
        $JoinSplat = @{}
        $StopRestart = $false

        # Current name
        $RegActiveComputerName = Get-ItemPropertyValue -Path HKLM:\SYSTEM\CurrentControlSet\Control\ComputerName\ActiveComputerName -Name ComputerName

        # Pending name
        $RegComputerName       = Get-ItemPropertyValue -Path HKLM:\SYSTEM\CurrentControlSet\Control\ComputerName\ComputerName -Name ComputerName

        $Win32ComputerSystem   = Get-CimInstance -ClassName Win32_ComputerSystem
        $Win32Domain           = $Win32ComputerSystem.Domain
        $Win32PartOfDomain     = $Win32ComputerSystem.PartOfDomain

        # Check if to rename computer
        if ($NewName -and
            $NewName -ne $RegComputerName -and
           (ShouldProcess @WhatIfSplat -Message "Renaming `"$Win32DnsHostName`" to `"$NewName`"" @VerboseSplat))
        {
            try
            {
                Rename-Computer -NewName $NewName

                $Result.Add('Renamed', $NewName)
                $JoinSplat.Add('Options', @('JoinWithNewName', 'AccountCreate'))
            }
            catch [Exception]
            {
                $StopRestart = $true
                throw $_
            }
        }

        # Check if domain credentials exist
        if ($JoinDomain -and $DomainCredential)
        {
            # Check if domain joined
            try
            {
                $DomainJoined = Test-ComputerSecureChannel
            }
            catch
            {
                $DomainJoined = $false
            }

            if ($JoinDomain -eq $Win32Domain -and
                (-not $DomainJoined -and $Win32PartOfDomain))
            {
                Write-Verbose @VerboseSplat -Message "Waiting to reboot `"$NewName`""
            }
            else
            {
                # Check if to leave another domain
                if (($JoinDomain -ne $Win32Domain) -and
                    (ShouldProcess @WhatIfSplat -Message "Removing `"$RegActiveComputerName`" from domain `"$Win32Domain`"." @VerboseSplat))
                {
                    Remove-Computer -WorkgroupName 'WORKGROUP' -Force
                    $DomainJoined = $false

                    <#
                    $InvokeCimParams =
                    @{
                        MethodName = 'UnjoinDomainOrWorkGroup'
                        Arguments =
                        @{
                            FUnjoinOptions=0
                        }
                    }

                    $Win32ComputerSystem | Invoke-CimMethod @InvokeCimParams > $null
                    #>
                }

                if (-not $DomainJoined -and
                    (ShouldProcess @WhatIfSplat -Message "Adding `"$NewName`" to domain `"$JoinDomain`"." @VerboseSplat))
                {
                    try
                    {
                        Add-Computer @JoinSplat -DomainName $JoinDomain -Credential $DomainCredential -Force

                        $Result.Add('JoinedDomain', $NewName)
                    }
                    catch [Exception]
                    {
                        $StopRestart = $true
                        throw $_
                    }
                }
            }
        }

        # Check if to restart
        if ($Restart.IsPresent -and -not $StopRestart -and
            ($Result.Count -gt 0 -or ($RegActiveComputerName -ne $RegComputerName)) -and
            (ShouldProcess @WhatIfSplat -Message "Restarting `"$NewName`"." @VerboseSplat))
        {
            Restart-Computer -Force
        }

        # ██████╗ ███████╗████████╗██╗   ██╗██████╗ ███╗   ██╗
        # ██╔══██╗██╔════╝╚══██╔══╝██║   ██║██╔══██╗████╗  ██║
        # ██████╔╝█████╗     ██║   ██║   ██║██████╔╝██╔██╗ ██║
        # ██╔══██╗██╔══╝     ██║   ██║   ██║██╔══██╗██║╚██╗██║
        # ██║  ██║███████╗   ██║   ╚██████╔╝██║  ██║██║ ╚████║
        # ╚═╝  ╚═╝╚══════╝   ╚═╝    ╚═════╝ ╚═╝  ╚═╝╚═╝  ╚═══╝

        if ($Result.Count -gt 0)
        {
            Write-Output -InputObject $Result
        }
    }
}

Process
{
    # ██████╗ ██████╗  ██████╗  ██████╗███████╗███████╗███████╗
    # ██╔══██╗██╔══██╗██╔═══██╗██╔════╝██╔════╝██╔════╝██╔════╝
    # ██████╔╝██████╔╝██║   ██║██║     █████╗  ███████╗███████╗
    # ██╔═══╝ ██╔══██╗██║   ██║██║     ██╔══╝  ╚════██║╚════██║
    # ██║     ██║  ██║╚██████╔╝╚██████╗███████╗███████║███████║
    # ╚═╝     ╚═╝  ╚═╝ ╚═════╝  ╚═════╝╚══════╝╚══════╝╚══════╝

    # Initialize
    $InvokeSplat = @{}

    # Setup remote
    if ($Session -and $Session.State -eq 'Opened')
    {
        # Load functions
        Invoke-Command -Session $Session -ErrorAction Stop -FilePath $PSScriptRoot\f_ShouldProcess.ps1

        # Get parameters
        Invoke-Command -Session $Session -ScriptBlock `
        {
            # Common
            $VerboseSplat = $Using:VerboseSplat
            $WhatIfSplat  = $Using:WhatIfSplat
            $Force        = $Using:Force

            # Mandatory parameters
            $NewName = $Using:NewName
            $JoinDomain = $Using:JoinDomain
            $DomainCredential = $Using:DomainCredential
            $Restart = $Using:Restart
        }

        # Set remote splat
        $InvokeSplat.Add('Session', $Session)
    }
    else # Setup Locally
    {
        Check-Continue -Message "Invoke locally?"

        # Load functions
        Invoke-Command -ScriptBlock `
        {
            try
            {
                . $PSScriptRoot\f_ShouldProcess.ps1
            }
            catch [Exception]
            {
                throw $_
            }

        } -NoNewScope

        # Set local splat
        $InvokeSplat.Add('NoNewScope', $true)
    }

    # Invoke
    try
    {
        # Run main
        $Result = Invoke-Command @InvokeSplat -ScriptBlock $MainScriptBlock -ErrorAction Stop

    }
    catch [Exception]
    {
        throw "$_ $($_.ScriptStackTrace)"
    }

    # ██████╗ ███████╗███████╗██╗   ██╗██╗  ████████╗
    # ██╔══██╗██╔════╝██╔════╝██║   ██║██║  ╚══██╔══╝
    # ██████╔╝█████╗  ███████╗██║   ██║██║     ██║
    # ██╔══██╗██╔══╝  ╚════██║██║   ██║██║     ██║
    # ██║  ██║███████╗███████║╚██████╔╝███████╗██║
    # ╚═╝  ╚═╝╚══════╝╚══════╝ ╚═════╝ ╚══════╝╚═╝

    if ($Result)
    {
        if ($Result.GetType().Name -eq 'Hashtable')
        {
            $ResultOutput = @{}

            foreach($item in $Result.GetEnumerator())
            {
                if ($item.Key.GetType().Name -eq 'String')
                {
                    $ResultOutput.Add($item.Key, $item.Value)
                }
            }

            Write-Output -InputObject $ResultOutput
        }
        else
        {
            Write-Warning -Message 'Unexpected result:'

            foreach($row in $Result)
            {
                Write-Host -Object $row
            }
        }
    }
}

End
{
}

