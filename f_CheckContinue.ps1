 .DESCRIPTION
    Prompt and check if to continue
 .NOTES
    AUTHOR Jonas Henriksson
 .LINK
    https://github.com/J0N7E
#>

function Check-Continue
{
    param
    (
        [string]$Message
    )

    if (-not ($Force.IsPresent -or $WhatIfSplat.Count))
    {
        Write-Warning -Message $Message

        if ((Read-Host -Prompt "Continue? [y/n]") -ne 'y')
        {
            break
        }
    }
}

